    private void loadBrandsToComboBox() {
        cmbBoxBrand.removeAllItems();

        EntityManager em = null;
        try {
            em = PersistenceManager.getEntityManager();
            TypedQuery<Brand> query = em.createQuery("SELECT b FROM Brand b WHERE b.active = true AND b.id > 1", Brand.class);
            List<Brand> brands = query.getResultList();

            for (Brand b : brands) {
                cmbBoxBrand.addItem(b);
            }
            if (!brands.isEmpty()) {
                cmbBoxBrand.setSelectedIndex(0);
                updateProductList((Brand) cmbBoxBrand.getSelectedItem());
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Error loading brands: " + e.getMessage());
        } finally {
            if (em != null && em.isOpen()) {
                em.close();
            }
        }
    }

    private void setupListeners() {
        cmbBoxBrand.addActionListener(evt -> {
            Brand selectedBrand = (Brand) cmbBoxBrand.getSelectedItem();
            if (selectedBrand != null) {
                updateProductList(selectedBrand);
            }
        });
    }

    private void updateProductList(Brand brand) {

        cmbProduct.removeAllItems();

        EntityManager em = null;
        try {
            em = PersistenceManager.getEntityManager();
            Brand managedBrand = em.find(Brand.class, brand.getId());
            if (managedBrand != null && managedBrand.getProducts() != null) {
                for (Product p : managedBrand.getProducts()) {
                    if (p.getActive()) {
                        cmbProduct.addItem(p);
                    }
                }
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Error loading products: " + e.getMessage());
        } finally {
            if (em != null && em.isOpen()) {
                em.close();
            }
        }

    }

