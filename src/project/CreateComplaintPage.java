    private void btnCreateComplaintActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCreateComplaintActionPerformed
        EntityManager em = null;
        try {
            em = PersistenceManager.getEntityManager();
            em.getTransaction().begin();

            Customer currentCustomer = em.find(Customer.class, ((Customer) UserManager.getUser()).getId());

            Brand selectedBrand = (Brand) cmbBoxBrand.getSelectedItem();
            Product selectedProduct = (Product) cmbProduct.getSelectedItem();
            String subject = (String) cmbBoxSubject.getSelectedItem();
            String detail = txtDetail.getText().trim();

            if (txtDetail.getText().isEmpty()) {
                JOptionPane.showMessageDialog(CreateComplaintPage.this, "Please fill in all fields.");
                return;
            }
            if (selectedProduct == null) {
                JOptionPane.showMessageDialog(CreateComplaintPage.this, "Selected product modal is not valid.");
                return;
            }

            if (selectedBrand == null) {
                JOptionPane.showMessageDialog(CreateComplaintPage.this, "Selected brand is not valid.");
                return;
            }

            TypedQuery<Long> countQuery = em.createQuery(
                    "SELECT COUNT(c) FROM Complaint c WHERE c.customer = :customer AND c.product = :product AND c.brand = :brand AND c.subject = :subject AND c.active = true",
                    Long.class);
            countQuery.setParameter("customer", currentCustomer);
            countQuery.setParameter("product", selectedProduct);
            countQuery.setParameter("brand", selectedBrand);
            countQuery.setParameter("subject", subject);
            Long existingCount = countQuery.getSingleResult();

            if (existingCount > 0) {
                JOptionPane.showMessageDialog(this, "You have already submitted an active complaint for this product and subject.");
                return;
            }

            // Create the complaint and add it to the system
            Complaint complaint = new Complaint(
                    currentCustomer,
                    selectedBrand,
                    selectedProduct,
                    subject,
                    txtDetail.getText(),
                    true);
            em.persist(complaint);
            em.getTransaction().commit();
            ComplaintFileLogger.logChange("[CREATE] New complaint created | "
                    + "Complaint ID: " + complaint.getId()
                    + " | Customer: " + complaint.getCustomer().getEmail()
                    + " | Product: " + complaint.getProduct().getName()
                    + " | Subject: " + complaint.getSubject()
                    + " | Brand: " + complaint.getBrand().getName()
                    + " | Status: " + complaint.getStatus()
                    + " | Created On: " + complaint.getDate());

            JOptionPane.showMessageDialog(CreateComplaintPage.this, "Complaint created successfully.");
            CreateComplaintPage.this.setVisible(false);
            CustomerPage customerPage = new CustomerPage();
            customerPage.setVisible(true);
        } catch (Exception e) {
            if (em != null && em.getTransaction().isActive()) {
                em.getTransaction().rollback();
            }
            JOptionPane.showMessageDialog(CreateComplaintPage.this, "An error occurred: " + e.getMessage());
        } finally {
            if (em != null && em.isOpen()) {
                em.close();
            }
        }
    }//GEN-LAST:event_btnCreateComplaintActionPerformed

