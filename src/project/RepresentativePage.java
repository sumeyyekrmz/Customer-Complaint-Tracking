    private void btnChangeStatusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnChangeStatusActionPerformed

        int selectedRow = jTable1.getSelectedRow();
        if (selectedRow != -1) {
            int complaintId = (int) complaintTable.getValueAt(selectedRow, 0); // ID sütunu indexi 0
            Complaint selectedComplaint = getComplaintById(complaintId);
            if (selectedComplaint != null) {
                CustomerRepresentative.selectedComplaint = selectedComplaint;
                this.setVisible(false);
                new ChangeStatusPage().setVisible(true);
            } else {
                JOptionPane.showMessageDialog(this, "Complaint could not be found.");
            }
        } else {
            JOptionPane.showMessageDialog(this, "Please select a complaint.");
        }

    }//GEN-LAST:event_btnChangeStatusActionPerformed

