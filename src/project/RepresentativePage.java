    private void loadComplaints(String statusFilter) {

        CustomerRepresentative currentUser = (CustomerRepresentative) UserManager.getUser();
        int brandId = currentUser.getAssignedBrand().getId();

        complaintTable.setRowCount(0);

        String sql = "SELECT c.id, c.complaint_date, cu.email, p.name, c.subject, c.status "
                + "FROM complaints c "
                + "JOIN users cu ON c.customer_id = cu.id "
                + "JOIN products p ON c.product_id = p.id "
                + "WHERE c.brand_id = ? AND c.active = true";

        if (!statusFilter.equals("All")) {
            sql += " AND c.status = ?";
        }

        try (Connection conn = DBConnection.getConnection(); PreparedStatement stmt = conn.prepareStatement(sql)) {

            stmt.setInt(1, brandId);
            if (!statusFilter.equals("All")) {
                stmt.setString(2, statusFilter);
            }

            try (ResultSet rs = stmt.executeQuery()) {
                while (rs.next()) {

                    complaintTable.addRow(new Object[]{
                        rs.getInt("id"),
                        rs.getDate("complaint_date"),
                        rs.getString("email"),
                        rs.getString("name"),
                        rs.getString("subject"),
                        rs.getString("status")
                    });

                }
            }
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(this, "Error loading complaints: " + e.getMessage());
        }

    }

    private Complaint getComplaintById(int id) {
        String sql = "SELECT c.*, cu.email AS customer_email, p.name AS product_name "
                + "FROM complaints c "
                + "JOIN users cu ON c.customer_id = cu.id "
                + "JOIN products p ON c.product_id = p.id "
                + "WHERE c.id = ?";

        try (Connection conn = DBConnection.getConnection(); PreparedStatement stmt = conn.prepareStatement(sql)) {
            stmt.setInt(1, id);

            try (ResultSet rs = stmt.executeQuery()) {
                if (rs.next()) {
                    Customer customer = new Customer();
                    customer.setId(rs.getInt("customer_id"));
                    customer.setEmail(rs.getString("customer_email"));
                    // customer için diğer alanlar set edilebilir

                    Product product = new Product();
                    product.setId(rs.getInt("product_id"));
                    product.setName(rs.getString("product_name"));
                    // product için diğer alanlar set edilebilir

                    Brand brand = new Brand();
                    brand.setId(rs.getInt("brand_id"));
                    // brand için diğer alanlar set edilebilir

                    Complaint complaint = new Complaint(
                            customer,
                            brand,
                            product,
                            rs.getString("subject"),
                            rs.getString("detail"),
                            rs.getBoolean("active")
                    );
                    complaint.setId(rs.getInt("id"));
                    complaint.setDate(rs.getDate("complaint_date").toLocalDate());
                    complaint.setStatus(rs.getString("status"));

                    return complaint;
                }
            }
        } catch (SQLException e) {
            e.printStackTrace();
        }
        return null;
    }

