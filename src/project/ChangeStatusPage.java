    public ChangeStatusPage() {
        super();
        initComponents();
        messagesModel.clear();

        EntityManager em = PersistenceManager.getEntityManager();
        try {
            // Complaint ID'sine göre güncel complaint nesnesini alıyoruz
            Complaint complaint = em.find(Complaint.class, CustomerRepresentative.selectedComplaint.getId());

            if (complaint != null) {
                CustomerRepresentative.selectedComplaint = complaint; // Güncellenmiş nesneyi güncelliyoruz

                // Çözümleri liste modeline ekle
                if (complaint.getSolutions() != null) {
                    for (String solution : complaint.getSolutions()) {
                        messagesModel.addElement(solution);
                    }
                }
                lblStatus.setText(complaint.getStatus());
                lblProductName.setText(complaint.getProduct().getName());
                lblSubject.setText(complaint.getSubject());
                lblDetail.setText(complaint.getDetail());
                lstMessages.setModel(messagesModel);
                if (complaint.getObjection() != null && !complaint.getObjection().isEmpty()) {
                    jLabel7.setVisible(true);
                    lblObjection.setVisible(true);
                    lblObjection.setText(complaint.getObjection());
                } else {
                    jLabel7.setVisible(false);
                    lblObjection.setVisible(false);
                }
            } else {
                JOptionPane.showMessageDialog(this, "Complaint not found in database.");
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Error loading complaint data: " + e.getMessage());
        } finally {
            em.close();
        }
    }

