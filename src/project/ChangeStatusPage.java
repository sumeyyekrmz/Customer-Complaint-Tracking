    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
        EntityManager em = PersistenceManager.getEntityManager();
        EntityTransaction tx = em.getTransaction();
        try {
            Complaint complaint = em.find(Complaint.class, CustomerRepresentative.selectedComplaint.getId());
            String selectedStatus = cmbBoxStatus.getSelectedItem().toString();
            String currentStatus = complaint.getStatus();
            tx.begin();
            if (complaint == null) {
                JOptionPane.showMessageDialog(ChangeStatusPage.this, "Complaint not found in database.");
                tx.rollback();
                return;
            }

            // If the current status is "Dispute", only "Resolved After Dispute" can be selected
            if (selectedStatus.equals("Resolved After Dispute") && !currentStatus.equals("Dispute")) {
                JOptionPane.showMessageDialog(ChangeStatusPage.this, "You can only change the status to 'Resolved After Dispute' from 'Dispute'.");
                return;
            }
            if (currentStatus.equals("Dispute") && !selectedStatus.equals("Resolved After Dispute")) {
                JOptionPane.showMessageDialog(ChangeStatusPage.this, "You can only change the status to 'Resolved After Dispute' from 'Dispute'.");
                return;
            }

            if (txtSolution.getText().isEmpty() && (selectedStatus.equals("Resolved") || selectedStatus.equals("Resolved After Dispute"))) {
                JOptionPane.showMessageDialog(ChangeStatusPage.this, "Please write a message.");
            } else {// The complaint is updated and saved into the system

                if (selectedStatus.equals("Resolved") || selectedStatus.equals("Resolved After Dispute")) {
                    complaint.setActive(false);
                    complaint.setStatus(selectedStatus);
                    complaint.addSolution(txtSolution.getText());

                    complaint.setRep((CustomerRepresentative) UserManager.getUser());
                    JOptionPane.showMessageDialog(ChangeStatusPage.this, "Complaint updated successfully.");
                    ChangeStatusPage.this.setVisible(false);
                    RepresentativePage repPage = new RepresentativePage();
                    repPage.setVisible(true);
                    return;
                }

                complaint.setStatus(selectedStatus);
                complaint.addSolution(txtSolution.getText());

                complaint.setRep((CustomerRepresentative) UserManager.getUser());
                JOptionPane.showMessageDialog(ChangeStatusPage.this, "Complaint updated successfully.");
                em.persist(complaint);
                tx.commit();
                ComplaintFileLogger.logChange("[UPDATE] Complaint status changed | ID: " + complaint.getId()
                        + " | New Status: " + complaint.getStatus()
                        + " | Changed by: " + UserManager.getUser().getEmail()
                        + " | Date: " + LocalDateTime.now());
            }
        } catch (Exception e) {
            if (tx.isActive()) {
                tx.rollback();
            }
            JOptionPane.showMessageDialog(ChangeStatusPage.this, "An error occurred: " + e.getMessage());
        } finally {
            em.close();
        }
        ChangeStatusPage.this.setVisible(false);
        RepresentativePage repPage = new RepresentativePage();
        repPage.setVisible(true);
    }//GEN-LAST:event_btnUpdateActionPerformed

