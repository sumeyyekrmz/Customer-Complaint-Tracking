    private void btnShowCusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnShowCusActionPerformed
        listUserTable.clear();
        customerTable.setRowCount(0);
        jTable1.setModel(customerTable);
        customerTable.setColumnIdentifiers(new String[]{"Name", "Surname", "Email", "Active"});
        for(Customer c : Customer.customers){
            customerTable.addRow(new Object[]{
                c.getFirstName(), 
                c.getLastName(), 
                c.getEmail(), 
                c.getActive()});
            listUserTable.add(c);
        }
    }//GEN-LAST:event_btnShowCusActionPerformed

    private void btnShowRepActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnShowRepActionPerformed
        listUserTable.clear();
        repTable.setRowCount(0); 
        jTable1.setModel(repTable);
        repTable.setColumnIdentifiers(new String[]{"Name", "Surname", "Email", "Brand", "Active"});
        for(CustomerRepresentative c : CustomerRepresentative.representatives){
            repTable.addRow(new Object[]{
                c.getFirstName(), 
                c.getLastName(), 
                c.getEmail(), 
                c.getAssignedBrand(), 
                c.getActive()});
            listUserTable.add(c);
        }              
    }//GEN-LAST:event_btnShowRepActionPerformed

    private void btnChngActivityActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnChngActivityActionPerformed
        Object[] options = {"NO", "YES"};
        User selectedUser;
        if (jTable1.getSelectedRow() != -1) {
            int state = JOptionPane.showOptionDialog(AdminPage.this, "Are you sure?", "Message",
                    JOptionPane.YES_NO_OPTION, JOptionPane.QUESTION_MESSAGE, null, options, options[1]);
            if (state == 1) {
                selectedUser = listUserTable.get(jTable1.getSelectedRow());
                selectedUser.setActive(!selectedUser.getActive());
            }
        } else {
            JOptionPane.showMessageDialog(AdminPage.this, "Please select an user.");
        }
    }//GEN-LAST:event_btnChngActivityActionPerformed

