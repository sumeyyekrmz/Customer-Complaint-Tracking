    private void btnShowCompActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnShowCompActionPerformed
        complaintTable.setRowCount(0);
        jTable1.setModel(complaintTable);
        complaintTable.setColumnIdentifiers(new String[]{"Active", "Created On", "Customer", "Last Updated By", "Brand", "Subject", "Status"});
        // Add all complaints to the table
        for(Complaint c : Complaint.allComplaints){
            String repEmail = (c.getRep() != null) ? c.getRep().getEmail() : "-";
            complaintTable.addRow(new Object[]{
                c.getActive(),
                c.getDate(), 
                c.getCustomer().getEmail(),
                repEmail,
                c.getBrand(),  
                c.getSubject(), 
                c.getStatus()});
        }
    }//GEN-LAST:event_btnShowCompActionPerformed

    private void btnDetailsActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDetailsActionPerformed
        if (jTable1.getSelectedRow() != -1) {
            CustomerRepresentative.selectedComplaint = Complaint.allComplaints.get(jTable1.getSelectedRow());
            this.setVisible(false);
            ComplaintDetailsPage complaintDetailsPage = new ComplaintDetailsPage();
            complaintDetailsPage.setVisible(true);
        } else {
            JOptionPane.showMessageDialog(AdminPage.this, "Please select a complaint.");
        }
    }//GEN-LAST:event_btnDetailsActionPerformed

